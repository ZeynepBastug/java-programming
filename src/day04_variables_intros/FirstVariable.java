package day04_variables_intros;

public class FirstVariable {
    public static void main (String[] args){
        int n;
        //assign/put value 10 to n
        n = 10;
        //print value of n variables
        System.out.println(n);
        System.out.println("n");

        int apples;
        apples = 50;
        System.out.println(apples);

        int bananas;
        bananas = 100;
        System.out.print(bananas);
        int  num1 = 50;
        int num2 = 200;
        num1 = num2;
        System.out.println(num1);
        System.out.println(num2);


    }
}
