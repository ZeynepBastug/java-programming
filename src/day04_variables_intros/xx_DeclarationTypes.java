package day04_variables_intros;

public class xx_DeclarationTypes {
    public static void main (String[] args){
        //declaration + assignment in single statement:
        int mangoes = 12;
        System.out.println(mangoes);

        int watermelons = 3;
        System.out.println(watermelons);

        watermelons = 24;
        System.out.println(watermelons);

        int peopleInCity, birdsOnTree, fishInLake;

        peopleInCity = 1000;
        birdsOnTree = 21;
        fishInLake = 100;

        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishInLake);

        int houses = 1000, cars = 5000;
        System.out.println(houses);
        System.out.println(cars);

    }
}
